//! Shared helpers for the deterministic and non-deterministic conformance
//! test suites.

use std::fs;
use std::path::PathBuf;

/// Returns the directory where the C tests wrote their artefact files.
/// Override at runtime with `CBOR_TEST_OUT_DIR=...`; defaults to
/// `<crate>/../out/`, where the C test Makefile drops them.
pub fn out_dir() -> PathBuf {
    if let Ok(s) = std::env::var("CBOR_TEST_OUT_DIR") {
        return PathBuf::from(s);
    }
    let mut p = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    p.pop(); // .../share/everparse/tests/cbor
    p.push("out");
    p
}

/// Read `<out_dir>/<api>_<name>.input.cbor`.
pub fn read_input(api: &str, name: &str) -> Vec<u8> {
    let mut p = out_dir();
    p.push(format!("{}_{}.input.cbor", api, name));
    fs::read(&p).unwrap_or_else(|e| {
        panic!(
            "[{api}] cannot read input artefact for test '{name}' at {p:?}: {e}\n\
             Did you run `make run-c` (or the cbor_tests_{{det,nondet}}.exe binaries) first?"
        )
    })
}

/// Read `<out_dir>/<api>_<name>.serialized.cbor`. The file only exists for
/// valid tests that succeeded the full pipeline on the C side.
pub fn read_serialized(api: &str, name: &str) -> Vec<u8> {
    let mut p = out_dir();
    p.push(format!("{}_{}.serialized.cbor", api, name));
    fs::read(&p).unwrap_or_else(|e| {
        panic!(
            "[{api}] cannot read serialized artefact for test '{name}' at {p:?}: {e}"
        )
    })
}

